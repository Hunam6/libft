#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*node;

	if (!lst || !f)
		return ;
	node = lst;
	while (node->next)
	{
		f(node->content);
		node = node->next;
	}
	f(node->content);
}
